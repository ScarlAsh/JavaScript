let txt = document.getElementById('PAR');

function ChangeFont(font)
{
    txt.style.fontFamily = font;
}

function ChangeAlign(align)
{
    txt.style.textAlign = align;
}

function ChangeHeight(height)
{
    txt.style.lineHeight = height;
}
function ChangeLSpace(space)
{
    txt.style.letterSpacing = space;
}
function ChangeIndent(indent)
{
    txt.style.textIndent = indent;
}
function ChangeTransform(trans)
{
    txt.style.textTransform = trans;
}
function ChangeDecorate(deco)
{
    txt.style.textDecoration = deco;
}
function ChangeBorder(bord)
{
    txt.style.border = bord;
}

function ChangeBorderColor(color)
{
    txt.style.borderColor = color;
}
